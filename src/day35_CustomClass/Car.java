package day35_CustomClass;

public class Car {

    public String brand;  // instance variables
    public String model;

    public void setInfo(String carBrand, String carModel){ // Instance methods
        brand = carBrand;
        model =carModel;
    }

}
/*
Car class:
		Attributes: instance variables
			brand, model, color, price, year....

		actions: instance methods
			driver(), stop()....
 */
